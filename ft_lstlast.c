#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	index;

	index = ft_lstsize(lst);
	if (lst == NULL)
		return (0);
	while (lst)
	{
		if (lst->next == NULL)
			return (lst);
		lst = lst->next;
	}
	return (lst);
}
